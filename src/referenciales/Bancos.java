    private void cargar_tabla() {
        DefaultTableModel tab = (DefaultTableModel) tabla.getModel();
        tab.setRowCount(0);
        try {
            // ilike busca las letras por mayusculas y minisculas
            bd.listar_datos("SELECT id_banco, banc_nombre, banc_ruc, banc_direc, banc_tel, banc_correo\n"
                    + "  FROM bancos where id_banco::text like'%" + txtbuscar.getText() + "%' or banc_nombre ilike '%" + txtbuscar.getText() + "%';");
            while (bd.resultado.next()) {
                tab.addRow(new Object[]{bd.resultado.getString("id_banco"),
                    bd.resultado.getString("banc_nombre"),
                    bd.resultado.getString("banc_ruc"),
                    bd.resultado.getString("banc_direc"),
                    bd.resultado.getString("banc_tel"),
                    bd.resultado.getString("banc_correo")
                });

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al cargar la tabla" + e);
        }
    }

