    private void grabar() {
        if (accion.equals("nuevo")) {
            //guardar
            try {
                String sql = "INSERT INTO bancos(id_banco, banc_nombre, banc_ruc, banc_direc, banc_tel, banc_correo)\n"
                        + "    VALUES (?, ?, ?, ?, ?, ?);";
                PreparedStatement p = bd.conexion.prepareStatement(sql);
                p.setInt(1, Integer.parseInt(txtcodigo.getText()));
                p.setString(2, txtdescripcion.getText());
                p.setString(3, txtruc.getText());
                p.setString(4, txtdireccion.getText());
                p.setString(5, txttelefono.getText());
                p.setString(6, txtcorreo.getText());
                p.execute();
                cargar_tabla();
                btcancelar.doClick();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al guardar los datos" + e);
            }

        } else {
            //modificar
            try {
                String sql = "UPDATE bancos\n"
                        + "   SET banc_nombre=?, banc_ruc=?, banc_direc=?, banc_tel=?, \n"
                        + "       banc_correo=?\n"
                        + " WHERE id_banco=?;";
                PreparedStatement p = bd.conexion.prepareStatement(sql);
                p.setInt(6, Integer.parseInt(txtcodigo.getText()));
                p.setString(1, txtdescripcion.getText());
                p.setString(2, txtruc.getText());
                p.setString(3, txtdireccion.getText());
                p.setString(4, txttelefono.getText());
                p.setString(5, txtcorreo.getText());
                p.execute();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al modificar el registro" + e);
            }

            cargar_tabla();
            btcancelar.doClick();

        }

    }

    private void recuperar_datos() {
        try {
            bd.listar_datos("SELECT id_banco, banc_nombre, banc_ruc, banc_direc, banc_tel, banc_correo\n"
                    + "  FROM bancos where id_banco=" + txtcodigo.getText() + ";");
            if (bd.resultado.next()) {
                txtdescripcion.setText(bd.resultado.getString("banc_nombre"));
                txtruc.setText(bd.resultado.getString("banc_ruc"));
                txtdireccion.setText(bd.resultado.getString("banc_direc"));
                txttelefono.setText(bd.resultado.getString("banc_tel"));
                txtcorreo.setText(bd.resultado.getString("banc_correo"));

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al recuperar la tabla" + e);
        }
    }
