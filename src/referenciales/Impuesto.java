    private void grabar() {
        if (accion.equals("nuevo")) {
            //guardar
            try {
                String sql = "INSERT INTO impuesto(\n"
                        + "            id_impuesto, imp_descri)\n"
                        + "    VALUES (?, ?);";
                PreparedStatement p = bd.conexion.prepareStatement(sql);
                p.setInt(1, Integer.parseInt(txtcodigo.getText()));
                p.setString(2, txtdescripcion.getText());
                p.execute();
                cargar_tabla();
                btcancelar.doClick();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al guardar los datos" + e);
            }

        } else {
            //modificar
            try {
                String sql = "UPDATE impuesto\n"
                        + "   SET  imp_descri=?\n"
                        + " WHERE id_impuesto=?;";
                PreparedStatement p = bd.conexion.prepareStatement(sql);
                p.setInt(2, Integer.parseInt(txtcodigo.getText()));
                p.setString(1, txtdescripcion.getText());
                p.execute();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al modificar el registro de stock" + e);
            }

            cargar_tabla();
            btcancelar.doClick();

        }

    }

    private void recuperar_datos() {
        try {
            bd.listar_datos("SELECT id_impuesto, imp_descri\n"
                    + "  FROM impuesto where id_impuesto=" + txtcodigo.getText() + ";");
            if (bd.resultado.next()) {
                txtdescripcion.setText(bd.resultado.getString("imp_descri"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al recuperar la tabla" + e);
        }
    }
