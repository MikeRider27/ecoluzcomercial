    private void grabar() {
        if (accion.equals("nuevo")) {
            //guardar
            try {
                String sql = "INSERT INTO timbrado(\n"
                        + "            id_timbrado, tim_numero, tim_fechainicio, tim_fechavencimiento, \n"
                        + "            inicio, ultimo, actual)\n"
                        + "    VALUES (?, ?,?, ?, \n"
                        + "            ?, ?, ?);";
                PreparedStatement p = bd.conexion.prepareStatement(sql);
                p.setInt(1, Integer.parseInt(txtcodigo.getText()));
                p.setString(2, txtdescripcion.getText());
                p.setDate(3, java.sql.Date.valueOf(convertir_fecha(txtfecha1.getText())));
                p.setDate(4, java.sql.Date.valueOf(convertir_fecha(txtfecha2.getText())));
                p.setString(5, txtinicio.getText());
                p.setString(6, txtultimo.getText());
                p.setInt(7, Integer.parseInt(txtinicio.getText()));
                p.execute();
                cargar_tabla();
                btcancelar.doClick();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al guardar los datos" + e);
            }

        } else {
            //modificar
            try {

                bd.retornar_ultimo("timbrado", "actual", txtactual);
                String sql = "UPDATE timbrado\n"
                        + "   SET  tim_numero=?, tim_fechainicio=?, tim_fechavencimiento=?, \n"
                        + "       inicio=?, ultimo=?, actual=?\n"
                        + " WHERE id_timbrado=?;";
                PreparedStatement p = bd.conexion.prepareStatement(sql);
                p.setInt(7, Integer.parseInt(txtcodigo.getText()));
                p.setString(1, txtdescripcion.getText());
                p.setDate(2, java.sql.Date.valueOf(convertir_fecha(txtfecha1.getText())));
                p.setDate(3, java.sql.Date.valueOf(convertir_fecha(txtfecha2.getText())));
                p.setString(4, txtinicio.getText());
                p.setString(5, txtultimo.getText());
                p.setInt(6, Integer.parseInt(txtactual.getText()));
                p.execute();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al modificar el registro" + e);
            }

            cargar_tabla();
            btcancelar.doClick();

        }

    }

    private String convertir_fecha(String fecha) {
        // 25-08-2016
        String dia = "", mes = "", anho = "";

        dia = fecha.substring(0, 2);
        mes = fecha.substring(3, 5);
        anho = fecha.substring(6, 10);
        fecha = anho + "-" + mes + "-" + dia;

        return fecha;
    }

    private void recuperar_datos() {
        try {
            bd.listar_datos("SELECT id_timbrado, tim_numero, tim_fechainicio, tim_fechavencimiento, \n"
                    + "       inicio, ultimo, actual\n"
                    + "  FROM timbrado where id_timbrado=" + txtcodigo.getText() + ";");
            if (bd.resultado.next()) {
                txtdescripcion.setText(bd.resultado.getString("tim_numero"));
                txtfecha1.setText(fec.format(bd.resultado.getDate("tim_fechainicio")));
                txtfecha2.setText(fec.format(bd.resultado.getDate("tim_fechavencimiento")));
                txtinicio.setText(bd.resultado.getString("inicio"));
                txtultimo.setText(bd.resultado.getString("ultimo"));

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al recuperar la tabla" + e);
        }
    }
