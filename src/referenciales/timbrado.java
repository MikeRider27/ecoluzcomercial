    private void btmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmodificarActionPerformed
        if (txtcodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Seleccione un registro", "Informe", JOptionPane.INFORMATION_MESSAGE);
        } else {
            accion = "modificar";
            btcancelar.setEnabled(true);
            bd.deshabilitar_botones(btnuevo, btmodificar, bteliminar, btguardar);
            habilitar_campos();
            txtdescripcion.requestFocus();

        }
    }//GEN-LAST:event_btmodificarActionPerformed

    private void bteliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bteliminarActionPerformed
        if (txtcodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Seleccione un registro", "Informe", JOptionPane.INFORMATION_MESSAGE);
        } else {
            int mensaje = JOptionPane.showConfirmDialog(null, "Desea eliminar el registro", "Informe", JOptionPane.YES_NO_OPTION);
            if (mensaje == JOptionPane.YES_OPTION) {
                try {
                    String sql = "delete from timbrado where id_timbrado=?";
                    PreparedStatement eli = bd.conexion.prepareStatement(sql);
                    eli.setInt(1, Integer.parseInt(txtcodigo.getText()));
                    eli.execute();

                    cargar_tabla();
                    limpiar_campos();

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error al eliminar la tabla" + e);
                }
            }

        }
    }//GEN-LAST:event_bteliminarActionPerformed

    private void btguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btguardarActionPerformed
        if (txtdescripcion.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe Ingresar Datos", "Informe", JOptionPane.INFORMATION_MESSAGE);
            txtdescripcion.setEnabled(true);
            txtdescripcion.requestFocus();
            return;
        }
        if (txtfecha1.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe Ingresar Datos", "Informe", JOptionPane.INFORMATION_MESSAGE);
            txtfecha1.setEnabled(true);
            txtfecha1.requestFocus();
            return;
        }
        if (txtinicio.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe Ingresar Datos", "Informe", JOptionPane.INFORMATION_MESSAGE);
            txtinicio.setEnabled(true);
            txtinicio.requestFocus();
            return;
        }
        if (txtfecha2.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe Ingresar Datos", "Informe", JOptionPane.INFORMATION_MESSAGE);
            txtfecha2.setEnabled(true);
            txtfecha2.requestFocus();
            return;
        }
        if (txtultimo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe Ingresar Datos", "Informe", JOptionPane.INFORMATION_MESSAGE);
            txtultimo.setEnabled(true);
            txtultimo.requestFocus();
            return;
        }

        grabar();
    }//GEN-LAST:event_btguardarActionPerformed

