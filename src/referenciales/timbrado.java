    private void cargar_tabla() {
        DefaultTableModel tab = (DefaultTableModel) tabla.getModel();
        tab.setRowCount(0);
        try {
            // ilike busca las letras por mayusculas y minisculas
            bd.listar_datos("SELECT id_timbrado, tim_numero, tim_fechainicio, tim_fechavencimiento, \n"
                    + "       inicio, ultimo, actual\n"
                    + "  FROM timbrado where id_timbrado::text like'%" + txtbuscar.getText() + "%' or tim_numero ilike '%" + txtbuscar.getText() + "%';");
            while (bd.resultado.next()) {
                tab.addRow(new Object[]{bd.resultado.getString("id_timbrado"),
                    bd.resultado.getString("tim_numero"),
                    bd.resultado.getString("tim_fechainicio"),
                    bd.resultado.getString("tim_fechavencimiento"),
                    bd.resultado.getString("inicio"),
                    bd.resultado.getString("ultimo"),
                    bd.resultado.getString("actual")
                });

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al cargar la tabla" + e);
        }
    }

