    private void verificar() {
        if (cbtipo.getSelectedItem().equals("Guaranies")) {
            txtguaranies.setEnabled(true);
            txtguaranies.requestFocus();
            txtnumerotarjeta.setEnabled(false);
            txtbancotarjeta.setEnabled(false);
            txtnumerocheque.setEnabled(false);
            txtbancocheque.setEnabled(false);
            txttarjeta.setEnabled(false);
            txtcheque.setEnabled(false);
        }
        if (cbtipo.getSelectedItem().equals("Tarjeta")) {
            txttarjeta.setEnabled(true);
            txtnumerotarjeta.setEnabled(true);
            txtbancotarjeta.setEnabled(true);
            txttarjeta.requestFocus();

            txtguaranies.setEnabled(false);
            txtcheque.setEnabled(false);

            txtnumerocheque.setEnabled(false);
            txtbancocheque.setEnabled(false);
        }
        if (cbtipo.getSelectedItem().equals("Cheque")) {
            txtcheque.setEnabled(true);
            txtcheque.requestFocus();
            txtnumerocheque.setEnabled(true);
            txtbancocheque.setEnabled(true);

            txtguaranies.setEnabled(false);
            txttarjeta.setEnabled(false);

            txtnumerotarjeta.setEnabled(false);
            txtbancotarjeta.setEnabled(false);

        }

    }

