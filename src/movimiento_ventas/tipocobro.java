    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtguaranies.getText().isEmpty()) {
            txtguaranies.setText("0");
        }
        if (txtcheque.getText().isEmpty()) {
            txtcheque.setText("0");
        }
        if (txttarjeta.getText().isEmpty()) {
            txttarjeta.setText("0");
        }
        if (txtbancocheque.getText().isEmpty()) {
            txtbancocheque.setText("0");
        }
        if (txtbancotarjeta.getText().isEmpty()) {
            txtbancotarjeta.setText("0");
        }
        if (txtnumerocheque.getText().isEmpty()) {
            txtnumerocheque.setText("0");
        }
        if (txtnumerotarjeta.getText().isEmpty()) {
            txtnumerotarjeta.setText("0");
        }
        int suma = 0;
        int vguaranies = 0;
        int vcheque = 0;
        int vtarjeta = 0;
        int vtotal = 0;
        int vbancocheque = 0;
        int vbancotarjeta = 0;
        vguaranies = Integer.parseInt(txtguaranies.getText().replace(".", ""));
        vcheque = Integer.parseInt(txtcheque.getText().replace(".", ""));
        vtarjeta = Integer.parseInt(txttarjeta.getText().replace(".", ""));
        suma = vguaranies + vcheque + vtarjeta;
        vtotal = Integer.parseInt(txtmonto.getText().replace(".", ""));
        vbancocheque = Integer.parseInt(txtbancocheque.getText());
        vbancotarjeta = Integer.parseInt(txtbancotarjeta.getText());
        if (suma < vtotal) {
            JOptionPane.showMessageDialog(null, "Verique Monto a Cobrar");

            return;

        }
        if (vcheque > 0) {
            JOptionPane.showMessageDialog(null, "Ingrese Datos de Cheque y Seleccione Banco");
            txtcheque.requestFocus();
            if (txtnumerocheque.getText().equals("0")) {
//                JOptionPane.showMessageDialog(null, "Ingrese Datos de Bancos");
                txtnumerocheque.requestFocus();
                return;
            }
            if (txtbancocheque.getText().equals("0")) {
//                JOptionPane.showMessageDialog(null, "Ingrese Datos de Bancos");
                txtbancocheque.requestFocus();
                return;
            }
        } else {
            if (vtarjeta > 0) {
                JOptionPane.showMessageDialog(null, "Ingrese Datos de Tarjeta y Seleccione Banco");
                txtnumerotarjeta.requestFocus();
                if (txtbancotarjeta.getText().equals("0")) {
//                JOptionPane.showMessageDialog(null, "Ingrese Banco");
                    txtnumerotarjeta.requestFocus();
                    return;
                }
            }
        }

        DefaultTableModel det = (DefaultTableModel) Ventas.tipocobro.getModel();

        det.addRow(new Object[]{Ventas.txtn.getText(), "1", 0, 0, txtguaranies.getText(),});
        det.addRow(new Object[]{Ventas.txtn.getText(), "2", txtbancocheque.getText(), txtnumerocheque.getText(), txtcheque.getText(),});
        det.addRow(new Object[]{Ventas.txtn.getText(), "3", txtbancotarjeta.getText(), txtnumerotarjeta.getText(), txttarjeta.getText()});
        if (Ventas.txtpresupuesto.getText().isEmpty()) {
            Ventas.txtpresupuesto.setText("0");
        }

        Ventas.btok.doClick();
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

