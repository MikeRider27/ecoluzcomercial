    private void cargar_tabla() {
        DefaultTableModel tab = (DefaultTableModel) tabla.getModel();
        tab.setRowCount(0);
        SimpleDateFormat formatofecha = new SimpleDateFormat("dd/MM/yyyy"); //visualiza la forma de fecha
        try {
            bd.listar_datos("SELECT id_factura,   id_cliente, id_presupuesto, fact_numero, fact_fecha, fact_monto, id_estado\n"
                    + "  FROM factura "
                    + " where id_estado=1 and(id_factura::text like '%" + txtbuscar.getText() + "%' or "
                    + " id_cliente::text like '%" + txtbuscar.getText() + "%' )order by id_factura");
            while (bd.resultado.next()) {
                tab.addRow(new Object[]{bd.resultado.getString("id_factura"),
                    formatofecha.format(bd.resultado.getDate("fact_fecha")),
                    bd.resultado.getString("id_cliente"),
                    bd.resultado.getString("fact_monto"),
                    bd.resultado.getString("id_presupuesto")
                });

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,
                    "Error al Cargar tabla " + e,
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }

    }
